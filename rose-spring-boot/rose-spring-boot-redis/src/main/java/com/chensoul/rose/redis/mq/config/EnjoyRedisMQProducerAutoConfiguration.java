package com.chensoul.rose.redis.mq.config;

import com.chensoul.rose.redis.config.RedisCacheConfig;
import com.chensoul.rose.redis.mq.RedisMQTemplate;
import com.chensoul.rose.redis.mq.interceptor.RedisMessageInterceptor;
import java.util.List;
import lombok.extern.slf4j.Slf4j;
import org.springframework.boot.autoconfigure.AutoConfiguration;
import org.springframework.context.annotation.Bean;
import org.springframework.data.redis.core.StringRedisTemplate;

/**
 * Redis 消息队列 Producer 配置类
 *
 * @author EnjoyIot
 */
@Slf4j
@AutoConfiguration(after = RedisCacheConfig.class)
public class EnjoyRedisMQProducerAutoConfiguration {

    @Bean
    public RedisMQTemplate redisMQTemplate(
            StringRedisTemplate redisTemplate, List<RedisMessageInterceptor> interceptors) {
        RedisMQTemplate redisMQTemplate = new RedisMQTemplate(redisTemplate);
        // 添加拦截器
        interceptors.forEach(redisMQTemplate::addInterceptor);
        return redisMQTemplate;
    }
}
